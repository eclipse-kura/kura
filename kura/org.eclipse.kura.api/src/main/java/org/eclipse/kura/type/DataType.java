package org.eclipse.kura.type;

/**
 * This contains all the required data type constants required for representing
 * Java data types as {@link TypedValue}
 */
public enum DataType {
	/** boolean type */
	BOOLEAN,

	/** byte type */
	BYTE,

	/** byte array type */
	BYTE_ARRAY,

	/** double array Type */
	DOUBLE,

	/** integer type */
	INTEGER,

	/** long type */
	LONG,

	/** short type */
	SHORT,

	/** string type */
	STRING
}
